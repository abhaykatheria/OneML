function InitializationSketch() {
  const [selectedRun, setSelectedRun] = useState(0)

  const sketch = useCallback(
    (p: p5) => {
      const dataRng = makeRng(99)
      const points: { x: number; y: number }[] = []
      const centers = [[150, 150], [400, 130], [280, 380]]
      for (const [cx, cy] of centers) {
        for (let i = 0; i < 25; i++) {
          points.push({ x: cx + randn(dataRng) * 45, y: cy + randn(dataRng) * 45 })
        }
      }

      const K = 3
      const nRuns = 5

      // Run K-means with different seeds
      function runKmeans(seed: number) {
        const rng = makeRng(seed)
        let centroids = Array.from({ length: K }, () => ({
          x: 50 + rng() * 500, y: 50 + rng() * 380,
        }))
        let assigns = new Array(points.length).fill(0)

        for (let iter = 0; iter < 20; iter++) {
          // Assign
          for (let i = 0; i < points.length; i++) {
            let bestD = Infinity, bestC = 0
            for (let c = 0; c < K; c++) {
              const d = (points[i].x - centroids[c].x) ** 2 + (points[i].y - centroids[c].y) ** 2
              if (d < bestD) { bestD = d; bestC = c }
            }
            assigns[i] = bestC
          }
          // Update
          const newCentroids = centroids.map(() => ({ x: 0, y: 0, n: 0 }))
          for (let i = 0; i < points.length; i++) {
            newCentroids[assigns[i]].x += points[i].x
            newCentroids[assigns[i]].y += points[i].y
            newCentroids[assigns[i]].n++
          }
          centroids = newCentroids.map((c, idx) =>
            c.n > 0 ? { x: c.x / c.n, y: c.y / c.n } : centroids[idx]
          )
        }

        // Compute inertia
        let inertia = 0
        for (let i = 0; i < points.length; i++) {
          const c = assigns[i]
          inertia += (points[i].x - centroids[c].x) ** 2 + (points[i].y - centroids[c].y) ** 2
        }
        return { centroids, assigns, inertia }
      }

      const runs = Array.from({ length: nRuns }, (_, i) => runKmeans(i * 137 + 7))

      p.setup = () => {
        const parent = (p as unknown as Record<string, HTMLElement>)._userNode
        const pw = parent ? parent.clientWidth : 700
        p.createCanvas(pw, 430)
      }

      p.draw = () => {
        p.background(15, 23, 42)
        const run = runs[selectedRun]

        // Points
        p.noStroke()
        for (let i = 0; i < points.length; i++) {
          const col = CLUSTER_COLORS[run.assigns[i] % CLUSTER_COLORS.length]
          p.fill(col[0], col[1], col[2], 200)
          p.ellipse(points[i].x, points[i].y, 8, 8)
        }

        // Centroids
        for (let c = 0; c < K; c++) {
          const col = CLUSTER_COLORS[c % CLUSTER_COLORS.length]
          p.stroke(255)
          p.strokeWeight(2)
          p.fill(col[0], col[1], col[2])
          const cx = run.centroids[c].x
          const cy = run.centroids[c].y
          const s = 12
          p.beginShape()
          p.vertex(cx, cy - s)
          p.vertex(cx + s, cy)
          p.vertex(cx, cy + s)
          p.vertex(cx - s, cy)
          p.endShape(p.CLOSE)
        }

        // Info bar at bottom
        const barY = p.height - 40
        const barW = (p.width - 40) / nRuns
        for (let r = 0; r < nRuns; r++) {
          const isSelected = r === selectedRun
          const isBest = runs[r].inertia === Math.min(...runs.map((rn) => rn.inertia))
          p.noStroke()
          p.fill(isSelected ? 99 : 30, isSelected ? 102 : 41, isSelected ? 241 : 59, isSelected ? 200 : 150)
          p.rect(20 + r * barW + 2, barY, barW - 4, 30, 4)
          p.fill(isSelected ? 255 : 180)
          p.textSize(10)
          p.textAlign(p.CENTER, p.CENTER)
          p.text(`Run ${r + 1}: ${runs[r].inertia.toFixed(0)}${isBest ? ' *' : ''}`, 20 + r * barW + barW / 2, barY + 15)
        }

        // Title
        p.noStroke()
        p.fill(226, 232, 240)
        p.textSize(13)
        p.textAlign(p.LEFT, p.TOP)
        p.text(`Run ${selectedRun + 1}  |  Inertia: ${run.inertia.toFixed(0)}`, 10, 10)
        p.fill(148, 163, 184)
        p.textSize(11)
        p.text('* = lowest inertia (best). Different initializations yield different results.', 10, 28)
      }
    },
    [selectedRun],
  )

  return (
    <P5Sketch
      sketch={sketch}
      height={430}
      controls={
        <div className="flex items-center gap-4 text-sm text-gray-300">
          <label className="flex items-center gap-2">
            Run:
            <input type="range" min={0} max={4} step={1} value={selectedRun}
              onChange={(e) => setSelectedRun(parseInt(e.target.value))} className="w-36" />
            <span className="w-10 font-mono">Run {selectedRun + 1}</span>
          </label>
        </div>
      }
    />
  )
}

