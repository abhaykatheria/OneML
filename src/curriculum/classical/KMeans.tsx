function KMeansInteractiveSketch() {
  const [k, setK] = useState(3)
  const [step, setStep] = useState(0)
  const pointsRef = useRef<{ x: number; y: number }[]>([])
  const [pointCount, setPointCount] = useState(0)
  const [_autoGenerated, setAutoGenerated] = useState(false)

  const generateData = useCallback(() => {
    const rng = makeRng(Date.now() % 100000)
    const pts: { x: number; y: number }[] = []
    const centers = [
      [150, 150], [400, 120], [250, 350],
      [500, 300], [100, 400],
    ]
    for (let c = 0; c < Math.min(k + 1, 5); c++) {
      for (let i = 0; i < 20; i++) {
        pts.push({
          x: centers[c][0] + randn(rng) * 50,
          y: centers[c][1] + randn(rng) * 50,
        })
      }
    }
    pointsRef.current = pts
    setPointCount(pts.length)
    setStep(0)
    setAutoGenerated(true)
  }, [k])

  const sketch = useCallback(
    (p: p5) => {
      const points = pointsRef.current
      let centroids: { x: number; y: number }[] = []
      let assignments: number[] = []
      let currentStep = 0

      function initCentroids() {
        if (points.length === 0) return
        const rng = makeRng(42)
        centroids = []
        // K-means++ initialization
        const idx0 = Math.floor(rng() * points.length)
        centroids.push({ ...points[idx0] })
        for (let c = 1; c < k; c++) {
          const dists = points.map((pt) => {
            let minD = Infinity
            for (const cen of centroids) {
              const d = (pt.x - cen.x) ** 2 + (pt.y - cen.y) ** 2
              if (d < minD) minD = d
            }
            return minD
          })
          const total = dists.reduce((a, b) => a + b, 0)
          let r = rng() * total
          let chosen = 0
          for (let i = 0; i < dists.length; i++) {
            r -= dists[i]
            if (r <= 0) { chosen = i; break }
          }
          centroids.push({ ...points[chosen] })
        }
        assignments = new Array(points.length).fill(0)
      }

      function assignStep() {
        for (let i = 0; i < points.length; i++) {
          let bestD = Infinity
          let bestC = 0
          for (let c = 0; c < centroids.length; c++) {
            const d = (points[i].x - centroids[c].x) ** 2 + (points[i].y - centroids[c].y) ** 2
            if (d < bestD) { bestD = d; bestC = c }
          }
          assignments[i] = bestC
        }
      }

      function updateStep() {
        for (let c = 0; c < centroids.length; c++) {
          let sx = 0, sy = 0, n = 0
          for (let i = 0; i < points.length; i++) {
            if (assignments[i] === c) { sx += points[i].x; sy += points[i].y; n++ }
          }
          if (n > 0) {
            centroids[c] = { x: sx / n, y: sy / n }
          }
        }
      }

      p.setup = () => {
        const parent = (p as unknown as Record<string, HTMLElement>)._userNode
        const pw = parent ? parent.clientWidth : 700
        p.createCanvas(pw, 450)
        initCentroids()
      }

      p.draw = () => {
        p.background(15, 23, 42)

        if (points.length === 0) {
          p.fill(148, 163, 184)
          p.textSize(14)
          p.textAlign(p.CENTER, p.CENTER)
          p.text('Click "Generate Data" to create sample points, or click on the canvas to add points manually', p.width / 2, p.height / 2)
          return
        }

        // Run steps up to current step
        if (currentStep !== step) {
          initCentroids()
          for (let s = 0; s < step; s++) {
            assignStep()
            updateStep()
          }
          assignStep() // always assign after last update
          currentStep = step
        }

        // Draw Voronoi-ish background if we have centroids and step > 0
        if (step > 0 && centroids.length > 0) {
          const cellSize = 8
          for (let px = 0; px < p.width; px += cellSize) {
            for (let py = 0; py < p.height; py += cellSize) {
              let bestD = Infinity
              let bestC = 0
              for (let c = 0; c < centroids.length; c++) {
                const d = (px - centroids[c].x) ** 2 + (py - centroids[c].y) ** 2
                if (d < bestD) { bestD = d; bestC = c }
              }
              const col = CLUSTER_COLORS[bestC % CLUSTER_COLORS.length]
              p.noStroke()
              p.fill(col[0], col[1], col[2], 20)
              p.rect(px, py, cellSize, cellSize)
            }
          }
        }

        // Draw points
        p.noStroke()
        for (let i = 0; i < points.length; i++) {
          const c = step > 0 ? assignments[i] : 0
          const col = step > 0
            ? CLUSTER_COLORS[c % CLUSTER_COLORS.length]
            : [148, 163, 184] as [number, number, number]
          p.fill(col[0], col[1], col[2], 200)
          p.ellipse(points[i].x, points[i].y, 8, 8)
        }

        // Draw centroids
        if (step > 0) {
          for (let c = 0; c < centroids.length; c++) {
            const col = CLUSTER_COLORS[c % CLUSTER_COLORS.length]
            p.stroke(255)
            p.strokeWeight(2)
            p.fill(col[0], col[1], col[2])
            // Draw as diamond
            const cx = centroids[c].x
            const cy = centroids[c].y
            const s = 10
            p.beginShape()
            p.vertex(cx, cy - s)
            p.vertex(cx + s, cy)
            p.vertex(cx, cy + s)
            p.vertex(cx - s, cy)
            p.endShape(p.CLOSE)
          }
        }

        // Info
        p.noStroke()
        p.fill(226, 232, 240)
        p.textSize(12)
        p.textAlign(p.LEFT, p.TOP)
        p.text(`K = ${k}  |  Step ${step}  |  ${points.length} points`, 10, 10)
        if (step > 0) {
          // Compute inertia
          let inertia = 0
          for (let i = 0; i < points.length; i++) {
            const c = assignments[i]
            if (centroids[c]) {
              inertia += (points[i].x - centroids[c].x) ** 2 + (points[i].y - centroids[c].y) ** 2
            }
          }
          p.fill(148, 163, 184)
          p.textSize(11)
          p.text(`Inertia: ${inertia.toFixed(0)}`, 10, 28)
        }
      }

      p.mousePressed = () => {
        if (p.mouseX > 0 && p.mouseX < p.width && p.mouseY > 0 && p.mouseY < p.height) {
          points.push({ x: p.mouseX, y: p.mouseY })
          setPointCount(points.length)
          setStep(0)
          currentStep = -1
        }
      }
    },
    [k, step, pointCount],
  )

  return (
    <P5Sketch
      sketch={sketch}
      height={450}
      controls={
        <div className="flex flex-wrap items-center gap-4 text-sm text-gray-300">
          <label className="flex items-center gap-2">
            K:
            <input type="range" min={2} max={8} step={1} value={k}
              onChange={(e) => { setK(parseInt(e.target.value)); setStep(0) }} className="w-28" />
            <span className="w-6 font-mono">{k}</span>
          </label>
          <label className="flex items-center gap-2">
            Step:
            <input type="range" min={0} max={15} step={1} value={step}
              onChange={(e) => setStep(parseInt(e.target.value))} className="w-28" />
            <span className="w-6 font-mono">{step}</span>
          </label>
          <button onClick={generateData}
            className="rounded bg-indigo-600 px-3 py-1 text-white hover:bg-indigo-500 transition-colors">
            Generate Data
          </button>
          <button onClick={() => { pointsRef.current = []; setPointCount(0); setStep(0); setAutoGenerated(false) }}
            className="rounded bg-gray-700 px-3 py-1 text-gray-300 hover:bg-gray-600 transition-colors">
            Clear
          </button>
        </div>
      }
    />
  )
}

