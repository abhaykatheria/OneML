function ElbowMethodSketch() {
  const sketch = useCallback((p: p5) => {
    const dataRng = makeRng(55)
    const points: { x: number; y: number }[] = []
    const trueCenters = [[2, 5], [5, 2], [8, 6], [6, 8]]
    for (const [cx, cy] of trueCenters) {
      for (let i = 0; i < 30; i++) {
        points.push({ x: cx + randn(dataRng) * 0.8, y: cy + randn(dataRng) * 0.8 })
      }
    }

    function runKmeans(kVal: number, seed: number) {
      const rng = makeRng(seed)
      let centroids = Array.from({ length: kVal }, () => ({
        x: rng() * 10, y: rng() * 10,
      }))
      let assigns = new Array(points.length).fill(0)

      for (let iter = 0; iter < 30; iter++) {
        for (let i = 0; i < points.length; i++) {
          let bestD = Infinity, bestC = 0
          for (let c = 0; c < kVal; c++) {
            const d = (points[i].x - centroids[c].x) ** 2 + (points[i].y - centroids[c].y) ** 2
            if (d < bestD) { bestD = d; bestC = c }
          }
          assigns[i] = bestC
        }
        const newCentroids = centroids.map(() => ({ x: 0, y: 0, n: 0 }))
        for (let i = 0; i < points.length; i++) {
          newCentroids[assigns[i]].x += points[i].x
          newCentroids[assigns[i]].y += points[i].y
          newCentroids[assigns[i]].n++
        }
        centroids = newCentroids.map((c, idx) =>
          c.n > 0 ? { x: c.x / c.n, y: c.y / c.n } : centroids[idx]
        )
      }

      let inertia = 0
      for (let i = 0; i < points.length; i++) {
        const c = assigns[i]
        inertia += (points[i].x - centroids[c].x) ** 2 + (points[i].y - centroids[c].y) ** 2
      }
      return inertia
    }

    const ks = Array.from({ length: 10 }, (_, i) => i + 1)
    const inertias = ks.map((kVal) => {
      // Run 3 times, take best
      const results = [runKmeans(kVal, 42), runKmeans(kVal, 99), runKmeans(kVal, 137)]
      return Math.min(...results)
    })

    let animFrame = 0

    p.setup = () => {
      const parent = (p as unknown as Record<string, HTMLElement>)._userNode
      const pw = parent ? parent.clientWidth : 700
      p.createCanvas(pw, 350)
      animFrame = 0
    }

    p.draw = () => {
      p.background(15, 23, 42)
      animFrame++
      const visibleK = Math.min(10, Math.floor(animFrame / 15) + 1)

      const margin = 60
      const plotW = p.width - margin * 2
      const plotH = p.height - margin * 2
      const maxInertia = inertias[0] * 1.1

      function mx(kVal: number): number { return margin + ((kVal - 1) / 9) * plotW }
      function my(val: number): number { return p.height - margin - (val / maxInertia) * plotH }

      // Axes
      p.stroke(51, 65, 85)
      p.strokeWeight(1)
      p.line(margin, p.height - margin, p.width - margin, p.height - margin)
      p.line(margin, margin, margin, p.height - margin)

      // Labels
      p.noStroke()
      p.fill(148, 163, 184)
      p.textSize(12)
      p.textAlign(p.CENTER, p.TOP)
      p.text('Number of Clusters (K)', p.width / 2, p.height - 18)
      p.push()
      p.translate(16, p.height / 2)
      p.rotate(-p.HALF_PI)
      p.textAlign(p.CENTER, p.BOTTOM)
      p.text('Inertia (Sum of Squared Distances)', 0, 0)
      p.pop()

      // Tick marks
      p.textSize(10)
      p.textAlign(p.CENTER, p.TOP)
      for (let kVal = 1; kVal <= 10; kVal++) {
        p.fill(100, 116, 139)
        p.text(kVal.toString(), mx(kVal), p.height - margin + 6)
      }

      // Curve
      p.noFill()
      p.stroke(99, 102, 241)
      p.strokeWeight(2)
      p.beginShape()
      for (let i = 0; i < visibleK; i++) {
        p.vertex(mx(ks[i]), my(inertias[i]))
      }
      p.endShape()

      // Points
      p.noStroke()
      for (let i = 0; i < visibleK; i++) {
        p.fill(99, 102, 241)
        p.ellipse(mx(ks[i]), my(inertias[i]), 8, 8)
      }

      // Elbow highlight at K=4
      if (visibleK >= 4) {
        p.stroke(250, 204, 21, 180)
        p.strokeWeight(1.5)
        ;(p.drawingContext as CanvasRenderingContext2D).setLineDash([5, 5])
        p.line(mx(4), margin, mx(4), p.height - margin)
        ;(p.drawingContext as CanvasRenderingContext2D).setLineDash([])

        p.noStroke()
        p.fill(250, 204, 21)
        p.textSize(12)
        p.textAlign(p.CENTER, p.TOP)
        p.text('Elbow (K=4)', mx(4), margin - 4)
      }

      // Title
      p.noStroke()
      p.fill(226, 232, 240)
      p.textSize(13)
      p.textAlign(p.LEFT, p.TOP)
      p.text('Elbow Method', margin, 6)
    }
  }, [])

  return <P5Sketch sketch={sketch} height={350} />
}

